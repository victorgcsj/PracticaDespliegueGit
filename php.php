<h3 class="letras luz">Características de Php</h3>


<p class="text-justify">
  <ul>
    <li>Orientado al desarrollo de aplicaciones web dinámicas con acceso a información almacenada en una base de datos.</li> 

    <br>

    <li>Es considerado un lenguaje fácil de aprender, ya que en su desarrollo se simplificaron distintas especificaciones, como es el caso de la definición de las 
        variables primitivas, ejemplo que se hace evidente en el uso de php arrays.</li>

    <br>

    <li>El código fuente escrito en PHP es invisible al navegador web y al cliente, ya que es el servidor el que se encarga de ejecutar el código y enviar su resultado 
        HTML al navegador.</li>

    <br>

    <li>Capacidad de conexión con la mayoría de los motores de base de datos que se utilizan en la actualidad, destaca su conectividad con MySQL y PostgreSQL.</li> 
        Capacidad de expandir su potencial utilizando módulos (llamados ext's o extensiones).
        Posee una amplia documentación en su sitio web oficial, entre la cual se destaca que todas las funciones del sistema están explicadas y ejemplificadas en un 
        único archivo de ayuda.</li>

    <br>

    <li>Es libre, por lo que se presenta como una alternativa de fácil acceso para todos.</li>
                    
    <br>

    <li>Permite aplicar técnicas de programación orientada a objetos.</li>

    <br>

    <li>No requiere definición de tipos de variables aunque sus variables se pueden evaluar también por el tipo que estén manejando en tiempo de ejecución.</li>

    <br>

    <li>Tiene manejo de excepciones (desde PHP5).</li>

    <br>

    <li>Si bien PHP no obliga a quien lo usa a seguir una determinada metodología a la hora de programar, aun haciéndolo, el programador37​ puede aplicar en su trabajo 
                    cualquier técnica de programación o de desarrollo que le permita escribir código ordenado, estructurado y manejable. Un ejemplo de esto son los desarrollos que 
                    en PHP se han hecho del patrón de diseño Modelo Vista Controlador (MVC), que permiten separar el tratamiento y acceso a los datos, la lógica de control y la 
                    interfaz de usuario en tres componentes independientes.</li>

    <br>

    <li>Debido a su flexibilidad ha tenido una gran acogida como lenguaje base para las aplicaciones WEB de manejo de contenido, y es su uso principal.</li>
  </ul>
</p>

